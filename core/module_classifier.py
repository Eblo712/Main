def _normalize_name(name: str) -> str:
    """Убирает расширение и приводит к нижнему регистру."""
    for ext in ('.dll', '.so', '.dylib', '.drv', '.sys', '.exe'):
        if name.endswith(ext):
            name = name[:-len(ext)]
            break
    return name.lower()

